CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE TABLE users (
                       users_uuid UUID DEFAULT uuid_generate_v4(),
                       users_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
                       users_login VARCHAR(50) NOT NULL UNIQUE,
                       users_name VARCHAR(50) NOT NULL,
                       users_last_name VARCHAR(50) NOT NULL,
                       users_role VARCHAR(50) NOT NULL,
                       users_password VARCHAR(50) NOT NULL,
                       users_email VARCHAR(50) NOT NULL,
                       users_salary_per_h BIGINT NOT NULL
)